# ADS-B/Mode-S emitter category -> icon key. A0-A7/B0-B4/C0 map 1:1 to the
# source's own per-category icon files. B5-B7 and the whole C/D range have
# no dedicated icon in the source set (the source's own b0.svg tooltip
# states it covers B0, B5, B6, B7) so they fall back to the closest
# generic icon -- see SOURCE.md for the drone/UAV (B6) gap this implies.
